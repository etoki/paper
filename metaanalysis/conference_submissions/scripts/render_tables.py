# ----------------------------------------------------------------------
def iceri_tables() -> str:
    out = ["# ICERI — auto-rendered tables\n",
           "Generated by `scripts/render_tables.py`.\n"]

    cross = WORKSPACE / "iceri" / "results" / "cross_tab_pools.csv"
    if cross.exists():
        out.append("## Table 1. Cross-tab cell-level pooled correlations (k ≥ 2 only)\n")
        rows = []
        with open(cross, encoding="utf-8") as f:
            reader = csv.DictReader(f)
            for row in reader:
                k = row.get("k", "0")
                try:
                    if int(float(k)) < 2:
                        continue
                except (ValueError, TypeError):
                    continue
                rows.append([
                    row["trait"],
                    row.get("level", ""),
                    row.get("discipline", ""),
                    fmt_int(k),
                    fmt_int(row.get("N_total", "")),
                    fmt_r(row.get("r_pooled", "")),
                    f"[{fmt_r(row.get('r_ci_lo', ''))}, {fmt_r(row.get('r_ci_hi', ''))}]",
                ])
        out.append(md_table(
            ["Trait", "Level", "Discipline", "k", "N", "r", "95% CI"], rows,
        ))

    inter = WORKSPACE / "iceri" / "results" / "interaction_terms.csv"
    if inter.exists():
        out.append("\n## Table 2. Long-format weighted-OLS interaction model\n")
        rows = []
        with open(inter, encoding="utf-8") as f:
            reader = csv.DictReader(f)
            for row in reader:
                rows.append([
                    row.get("term", ""),
                    fmt_r(row.get("coef", row.get("estimate", ""))),
                    fmt_r(row.get("se", "")),
                    fmt_q(row.get("t", row.get("z", ""))),
                    fmt_p(row.get("p", "")),
                ])
        out.append(md_table(["Term", "β (Fisher z)", "SE", "t / z", "p"], rows))

    return "\n".join(out)


def main():
    n_written = 0
    for venue, render in [("ecel", ecel_tables), ("iceel", iceel_tables), ("iceri", iceri_tables)]:
        out_path = WORKSPACE / venue / "results" / "tables.md"
        text = render()
        out_path.write_text(text, encoding="utf-8")
        print(f"Wrote {out_path} ({len(text.splitlines())} lines)")
        n_written += 1
    print(f"\n{n_written} table-files emitted.")
    sys.exit(0)


if __name__ == "__main__":
    main()
